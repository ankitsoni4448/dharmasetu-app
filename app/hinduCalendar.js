export function getUpcomingFestivals(days = 30) {
  const today = new Date(); today.setHours(0,0,0,0);
  const future = new Date(); future.setDate(future.getDate() + days);
  return HINDU_CALENDAR.filter(e => {
    const d = new Date(e.date);
    return d >= today && d <= future;
  }).sort((a,b) => new Date(a.date) - new Date(b.date));
}

export function getTodaysFestivals() {
  const today = new Date().toISOString().split('T')[0];
  return HINDU_CALENDAR.filter(e => e.date === today);
}

export function getNextMajorFestival() {
  const today = new Date(); today.setHours(0,0,0,0);
  return HINDU_CALENDAR.find(e => e.major && new Date(e.date) >= today);
}

export function getNextEkadashi() {
  const today = new Date(); today.setHours(0,0,0,0);
  return HINDU_CALENDAR.find(e => e.type === 'ekadashi' && new Date(e.date) >= today);
}

export function getFestivalsForDeity(deity) {
  const today = new Date(); today.setHours(0,0,0,0);
  return HINDU_CALENDAR.filter(e => e.deity === deity && new Date(e.date) >= today);
}

export function daysUntil(dateString) {
  const today = new Date(); today.setHours(0,0,0,0);
  const eventDate = new Date(dateString); eventDate.setHours(0,0,0,0);
  return Math.ceil((eventDate - today) / (1000 * 60 * 60 * 24));
}